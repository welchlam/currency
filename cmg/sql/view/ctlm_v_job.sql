CREATE OR REPLACE VIEW CTLM_V_JOB
(
  ACTIVE_FROM
, ACTIVE_TILL
, APPLICATION
, APR
, AUG
, AUTHOR
, AUTOARCH
, CHANGE_DATE
, CHANGE_TIME
, CHANGE_USERID
, CMDLINE
, CONFCAL
, CONFIRM
, CREATION_DATE
, CREATION_TIME
, CREATION_USER
, CRITICAL
, CYCLIC
, CYCLIC_TOLERANCE
, CYCLIC_TYPE
, DAYS
, DAYSCAL
, DAYS_AND_OR
, DEC
, DESCRIPTION
, DOCLIB
, DOCMEM
, FEB
, GROUP_NAME
, IND_CYCLIC
, INSTREAM_JCL
, INTERVAL
, JAN
, JOBNAME
, JUL
, JUN
, MAR
, MAXDAYS
, MAXRERUN
, MAXRUNS
, MAXWAIT
, MAY
, MEMLIB
, MEMNAME
, MULTY_AGENT
, NODEID
, NOV
, OCT
, JOB_OPTION
, OVERLIB
, OWNER
, PAR
, PARENT_TABLE
, PRIORITY
, RETRO
, RULE_BASED_CALENDAR_REL
, SEP
, SHIFT
, SHIFTNUM
, SYSDB
, TASKTYPE
, TIMEFROM
, TIMETO
, TIMEZONE
, USE_INSTREAM_JCL
, WEEKDAYS
, WEEKSCAL
)
AS
SELECT
  cj.ACTIVE_FROM
, cj.ACTIVE_TILL
, cj.APPLICATION
, cj.APR
, cj.AUG
, cj.AUTHOR
, cj.AUTOARCH
, TO_CHAR(cj.AMENDED_TSMP,'YYYYMMDD')
, TO_CHAR(cj.AMENDED_TSMP,'HH24MISS')
, cj.AMENDED_BY_NAME
, cj.CMDLINE
, cj.CONFCAL
, cj.CONFIRM
, TO_CHAR(cj.CREATED_TSMP,'YYYYMMDD')
, TO_CHAR(cj.CREATED_TSMP,'HH24MISS')
, cj.CREATED_BY_NAME
, cj.CRITICAL
, cj.CYCLIC
, cj.CYCLIC_TOLERANCE
, cj.CYCLIC_TYPE
, cj.DAYS
, cj.DAYSCAL
, cj.DAYS_AND_OR
, cj.DEC
, cj.DESCRIPTION
, cj.DOCLIB
, cj.DOCMEM
, cj.FEB
, cj.GROUP_NAME
, cj.IND_CYCLIC
, cj.INSTREAM_JCL
, cj.CJ_INTERVAL
, cj.JAN
, cj.JOBNAME
, cj.JUL
, cj.JUN
, cj.MAR
, cj.MAXDAYS
, cj.MAXRERUN
, cj.MAXRUNS
, cj.MAXWAIT
, cj.MAY
, cj.MEMLIB
, cj.MEMNAME
, cj.MULTY_AGENT
, cj.NODEID
, cj.NOV
, cj.OCT
, cj.JOB_OPTION
, cj.OVERLIB
, cj.OWNER
, cj.PAR
, cj.PARENT_TABLE
, cj.PRIORITY
, cj.RETRO
, cj.RULE_BASED_CALENDAR_REL
, cj.SEP
, cj.SHIFT
, cj.SHIFTNUM
, cj.SYSDB
, cj.TASKTYPE
, cj.TIMEFROM
, cj.TIMETO
, cj.TIMEZONE
, cj.USE_INSTREAM_JCL
, cj.WEEKDAYS
, cj.WEEKSCAL
FROM CTLM_JOB cj
INNER JOIN CTLM_GROUP cg
   ON cj.GROUP_NAME = cg.GROUP_NAME
   AND cg.ACTIVE_FLAG = 'A'
;
